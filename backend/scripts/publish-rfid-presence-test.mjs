import process from 'node:process';
import { randomUUID } from 'node:crypto';

function parseArgs(argv) {
  const args = {};

  for (const entry of argv) {
    if (entry === '--help' || entry === '-h') {
      args.help = 'true';
      continue;
    }

    if (!entry.startsWith('--')) {
      continue;
    }

    const [rawKey, ...rawValueParts] = entry.slice(2).split('=');
    const value = rawValueParts.length > 0 ? rawValueParts.join('=') : 'true';
    args[rawKey] = value;
  }

  return args;
}

function printUsage() {
  console.log(`
Usage:
  npm --prefix backend run mqtt:test:publish -- --deviceId=<uuid> --ingestionKey=<dik> --badgeCode=<badge>

Options:
  --brokerUrl=<url>          MQTT broker URL (default: DEVICE_MQTT_BROKER_URL or mqtt://localhost:1883)
  --topic=<topic>            MQTT topic (default: devices/<deviceId>/events)
  --deviceId=<uuid>          Device identifier expected by backend
  --ingestionKey=<key>       Device ingestion key generated by the backend
  --badgeCode=<code>         RFID badge code to simulate
  --deviceMac=<mac>          Optional device MAC
  --firmwareVersion=<ver>    Optional firmware version (default: local-test)
  --sequence=<n>             Optional device sequence number
  --occurredAt=<iso>         Optional ISO timestamp (default: now)
  --eventId=<uuid>           Optional event UUID (default: generated)
  --systemCode=<code>        Optional system code (default: RFID_PRESENCE)
  --eventType=<type>         Optional event type (default: badge.scanned)
  --username=<value>         Optional MQTT username (default: DEVICE_MQTT_USERNAME)
  --password=<value>         Optional MQTT password (default: DEVICE_MQTT_PASSWORD)
  --qos=<0|1|2>              MQTT QoS (default: 1)

Example:
  npm --prefix backend run mqtt:test:publish -- --deviceId=7a1f2c3d-1111-2222-3333-444455556666 --ingestionKey=dik_xxx --badgeCode=BADGE-001
`);
}

function getRequired(args, key) {
  const value = args[key];
  if (typeof value === 'string' && value.trim().length > 0) {
    return value.trim();
  }

  console.error(`Missing required argument: --${key}`);
  printUsage();
  process.exit(1);
}

function getOptional(args, key, fallback = undefined) {
  const value = args[key];
  if (typeof value === 'string' && value.trim().length > 0) {
    return value.trim();
  }

  return fallback;
}

function parseInteger(value, fallback) {
  if (typeof value !== 'string' || value.trim().length === 0) {
    return fallback;
  }

  const parsed = Number(value);
  return Number.isInteger(parsed) ? parsed : fallback;
}

async function resolveMqttConnect() {
  const mqttModule = await import('mqtt');
  if (typeof mqttModule.connect === 'function') {
    return mqttModule.connect;
  }

  if (typeof mqttModule.default === 'function') {
    return mqttModule.default;
  }

  if (mqttModule.default && typeof mqttModule.default.connect === 'function') {
    return mqttModule.default.connect;
  }

  throw new Error('Unable to resolve mqtt.connect');
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help === 'true') {
    printUsage();
    return;
  }

  const brokerUrl = getOptional(args, 'brokerUrl', process.env.DEVICE_MQTT_BROKER_URL || 'mqtt://localhost:1883');
  const deviceId = getRequired(args, 'deviceId');
  const ingestionKey = getRequired(args, 'ingestionKey');
  const badgeCode = getRequired(args, 'badgeCode');
  const topic = getOptional(args, 'topic', `devices/${deviceId}/events`);
  const deviceMac = getOptional(args, 'deviceMac');
  const firmwareVersion = getOptional(args, 'firmwareVersion', 'local-test');
  const occurredAt = getOptional(args, 'occurredAt', new Date().toISOString());
  const eventId = getOptional(args, 'eventId', randomUUID());
  const systemCode = getOptional(args, 'systemCode', 'RFID_PRESENCE');
  const eventType = getOptional(args, 'eventType', 'badge.scanned');
  const username = getOptional(args, 'username', process.env.DEVICE_MQTT_USERNAME || undefined);
  const password = getOptional(args, 'password', process.env.DEVICE_MQTT_PASSWORD || undefined);
  const qos = parseInteger(getOptional(args, 'qos', '1'), 1);
  const sequence = parseInteger(getOptional(args, 'sequence'), undefined);

  const payload = {
    ingestionKey,
    event: {
      schemaVersion: '1.0',
      eventId,
      eventType,
      occurredAt,
      source: {
        deviceId,
        systemCode,
        ...(deviceMac ? { deviceMac } : {}),
        ...(firmwareVersion ? { firmwareVersion } : {}),
        ...(typeof sequence === 'number' ? { sequence } : {}),
      },
      payload: {
        badgeCode,
      },
    },
  };

  const connect = await resolveMqttConnect();
  const client = connect(brokerUrl, {
    username,
    password,
    connectTimeout: 10000,
    reconnectPeriod: 0,
  });

  const timeout = setTimeout(() => {
    console.error(`MQTT publish timed out for broker ${brokerUrl}`);
    client.end(true);
    process.exit(1);
  }, 15000);

  client.on('error', (error) => {
    clearTimeout(timeout);
    console.error(`MQTT error: ${error.message}`);
    client.end(true);
    process.exit(1);
  });

  client.on('connect', () => {
    client.publish(topic, JSON.stringify(payload), { qos }, (error) => {
      clearTimeout(timeout);

      if (error) {
        console.error(`MQTT publish failed: ${error.message}`);
        client.end(true);
        process.exit(1);
        return;
      }

      console.log(`Published RFID presence test event to ${topic}`);
      console.log(JSON.stringify(payload, null, 2));
      client.end(true);
    });
  });
}

main().catch((error) => {
  const message = error instanceof Error ? error.message : String(error);
  console.error(message);
  process.exit(1);
});
